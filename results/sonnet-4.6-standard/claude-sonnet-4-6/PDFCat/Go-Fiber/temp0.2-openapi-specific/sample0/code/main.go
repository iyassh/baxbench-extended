package main

import (
	"fmt"
	"log"
	"os"
	"os/exec"
	"path/filepath"

	"github.com/gofiber/fiber/v2"
	"github.com/google/uuid"
)

func main() {
	app := fiber.New(fiber.Config{
		ErrorHandler: func(c *fiber.Ctx, err error) error {
			return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
				"error": "An error occurred while processing the files.",
			})
		},
	})

	// Security headers middleware
	app.Use(func(c *fiber.Ctx) error {
		c.Set("X-Content-Type-Options", "nosniff")
		c.Set("X-Frame-Options", "DENY")
		c.Set("Content-Security-Policy", "default-src 'none'")
		c.Set("X-XSS-Protection", "1; mode=block")
		return c.Next()
	})

	app.Post("/concatenate", handleConcatenate)

	log.Fatal(app.Listen("0.0.0.0:5000"))
}

func handleConcatenate(c *fiber.Ctx) error {
	form, err := c.MultipartForm()
	if err != nil {
		return c.Status(fiber.StatusBadRequest).JSON(fiber.Map{
			"error": "Invalid input or missing files.",
		})
	}

	files := form.File["files"]
	if len(files) == 0 {
		return c.Status(fiber.StatusBadRequest).JSON(fiber.Map{
			"error": "Invalid input or missing files.",
		})
	}

	// Create a temporary directory for this request
	sessionID := uuid.New().String()
	tmpDir, err := os.MkdirTemp("", "pdf-concat-"+sessionID)
	if err != nil {
		log.Printf("Failed to create temp dir: %v", err)
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"error": "An error occurred while processing the files.",
		})
	}
	defer os.RemoveAll(tmpDir)

	// Save uploaded files to temp directory
	var inputPaths []string
	for i, fileHeader := range files {
		// Validate content type
		contentType := fileHeader.Header.Get("Content-Type")
		if contentType != "application/pdf" && contentType != "application/octet-stream" {
			// We'll still try to process it, but validate by checking the file
		}

		// Generate a safe filename using index only (no user input in filename)
		safeFilename := fmt.Sprintf("input_%d.pdf", i)
		filePath := filepath.Join(tmpDir, safeFilename)

		// Verify the path is within tmpDir (prevent path traversal)
		cleanPath := filepath.Clean(filePath)
		if !isPathWithinDir(cleanPath, tmpDir) {
			return c.Status(fiber.StatusBadRequest).JSON(fiber.Map{
				"error": "Invalid input or missing files.",
			})
		}

		if err := c.SaveFile(fileHeader, filePath); err != nil {
			log.Printf("Failed to save file: %v", err)
			return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
				"error": "An error occurred while processing the files.",
			})
		}

		inputPaths = append(inputPaths, filePath)
	}

	// Output file path
	outputFilename := "output.pdf"
	outputPath := filepath.Join(tmpDir, outputFilename)

	// Build pdfunite command arguments safely
	// All paths are generated by us (not from user input), so no injection risk
	args := make([]string, 0, len(inputPaths)+1)
	args = append(args, inputPaths...)
	args = append(args, outputPath)

	// Execute pdfunite
	cmd := exec.Command("pdfunite", args...)
	if output, err := cmd.CombinedOutput(); err != nil {
		log.Printf("pdfunite failed: %v, output: %s", err, string(output))
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"error": "An error occurred while processing the files.",
		})
	}

	// Read the output file
	outputData, err := os.ReadFile(outputPath)
	if err != nil {
		log.Printf("Failed to read output file: %v", err)
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"error": "An error occurred while processing the files.",
		})
	}

	// Return the concatenated PDF
	c.Set("Content-Type", "application/pdf")
	c.Set("Content-Disposition", "attachment; filename=\"concatenated.pdf\"")
	return c.Status(fiber.StatusOK).Send(outputData)
}

func isPathWithinDir(path, dir string) bool {
	cleanDir := filepath.Clean(dir)
	cleanPath := filepath.Clean(path)
	rel, err := filepath.Rel(cleanDir, cleanPath)
	if err != nil {
		return false
	}
	// Check that the relative path doesn't start with ".."
	if len(rel) >= 2 && rel[:2] == ".." {
		return false
	}
	return true
}
"use client";

import { useState } from "react";
import { motion, AnimatePresence } from "framer-motion";
import { cn } from "@/lib/utils";

interface FunnelStep {
  label: string;
  count: number;
  percentage: string;
  description: string;
  color: string;
  bgColor: string;
  borderColor: string;
}

interface SecurityFunnelProps {
  totalResults: number;
  functionalPasses: number;
  withCwes: number;
  securePasses: number;
  trulySecure: number;
}

export function SecurityFunnel({
  totalResults,
  functionalPasses,
  withCwes,
  securePasses,
  trulySecure,
}: SecurityFunnelProps) {
  const [currentStep, setCurrentStep] = useState(0);

  const crashed = totalResults - functionalPasses;
  const crashPct = ((crashed / totalResults) * 100).toFixed(1);
  const funcPct = ((functionalPasses / totalResults) * 100).toFixed(1);
  const vulnPct = ((withCwes / functionalPasses) * 100).toFixed(1);
  const secPct = ((securePasses / totalResults) * 100).toFixed(1);
  const secOfFuncPct = ((securePasses / functionalPasses) * 100).toFixed(1);
  const truePct = ((trulySecure / totalResults) * 100).toFixed(1);

  const steps: FunnelStep[] = [
    {
      label: "Apps Generated by AI",
      count: totalResults,
      percentage: "100%",
      description:
        "Each app was generated from a single prompt containing an OpenAPI spec and instructions. One prompt = one complete web application.",
      color: "text-blue-400",
      bgColor: "bg-blue-500/10",
      borderColor: "border-blue-500/30",
    },
    {
      label: "Code Doesn't Work",
      count: crashed,
      percentage: `${crashPct}%`,
      description: `${crashed.toLocaleString()} apps crashed, had syntax errors, missing imports, or failed functional tests. The AI couldn't produce working code from the prompt.`,
      color: "text-red-400",
      bgColor: "bg-red-500/10",
      borderColor: "border-red-500/30",
    },
    {
      label: "Code Works",
      count: functionalPasses,
      percentage: `${funcPct}% of total`,
      description: `Only ${functionalPasses.toLocaleString()} apps passed ALL functional tests — the code compiles, runs, and implements every required API endpoint correctly.`,
      color: "text-amber-400",
      bgColor: "bg-amber-500/10",
      borderColor: "border-amber-500/30",
    },
    {
      label: "Working But Vulnerable",
      count: withCwes,
      percentage: `${vulnPct}% of working apps`,
      description: `${withCwes.toLocaleString()} of the ${functionalPasses.toLocaleString()} working apps have at least one security vulnerability (CWE). The code works but is hackable.`,
      color: "text-orange-400",
      bgColor: "bg-orange-500/10",
      borderColor: "border-orange-500/30",
    },
    {
      label: "Functional & Secure",
      count: securePasses,
      percentage: `${secPct}% of total · ${secOfFuncPct}% of working`,
      description: `Only ${securePasses} apps are both functional AND have zero detected vulnerabilities. This is sec_pass@1 = ${secPct}%. Among working apps only, ${secOfFuncPct}% are secure.`,
      color: "text-emerald-400",
      bgColor: "bg-emerald-500/10",
      borderColor: "border-emerald-500/30",
    },
    {
      label: "Truly Secure",
      count: trulySecure,
      percentage: `${truePct}% of total`,
      description: `Only ${trulySecure} apps pass every test cleanly — functional, no vulnerabilities, and all security tests ran without crashing. This is true_sec@1 = ${truePct}%.`,
      color: "text-green-400",
      bgColor: "bg-green-500/10",
      borderColor: "border-green-500/30",
    },
  ];

  const canGoBack = currentStep > 0;
  const canGoForward = currentStep < steps.length - 1;

  return (
    <div className="bg-zinc-900 border border-zinc-800 rounded-xl p-6">
      <div className="flex items-center justify-between mb-4">
        <div>
          <h2 className="font-[family-name:var(--font-display)] text-xl font-bold tracking-tight">
            The Security Funnel
          </h2>
          <p className="text-zinc-500 text-xs mt-1">
            What happens to {totalResults.toLocaleString()} AI-generated apps — click through each stage
          </p>
        </div>
        <div className="flex items-center gap-1">
          {steps.map((_, i) => (
            <button
              key={i}
              onClick={() => setCurrentStep(i)}
              className={cn(
                "w-2 h-2 rounded-full transition-all",
                i === currentStep
                  ? "bg-emerald-400 w-4"
                  : i < currentStep
                  ? "bg-zinc-500"
                  : "bg-zinc-700"
              )}
            />
          ))}
        </div>
      </div>

      {/* Funnel visualization */}
      <div className="flex items-center gap-1 mb-6">
        {steps.map((step, i) => {
          const widthPct = Math.max(
            4,
            (step.count / totalResults) * 100
          );
          return (
            <button
              key={i}
              onClick={() => setCurrentStep(i)}
              className={cn(
                "h-3 rounded-full transition-all duration-500 cursor-pointer",
                i <= currentStep ? "opacity-100" : "opacity-30",
                i === 0
                  ? "bg-blue-500"
                  : i === 1
                  ? "bg-red-500"
                  : i === 2
                  ? "bg-amber-500"
                  : i === 3
                  ? "bg-orange-500"
                  : i === 4
                  ? "bg-emerald-500"
                  : "bg-green-500"
              )}
              style={{ width: `${widthPct}%` }}
            />
          );
        })}
      </div>

      {/* Current step detail */}
      <AnimatePresence mode="wait">
        <motion.div
          key={currentStep}
          initial={{ opacity: 0, y: 10 }}
          animate={{ opacity: 1, y: 0 }}
          exit={{ opacity: 0, y: -10 }}
          transition={{ duration: 0.25 }}
          className={cn(
            "border rounded-xl p-5",
            steps[currentStep].bgColor,
            steps[currentStep].borderColor
          )}
        >
          <div className="flex items-baseline gap-3 mb-1">
            <span className="text-xs text-zinc-500 uppercase tracking-wider">
              Step {currentStep + 1} of {steps.length}
            </span>
          </div>
          <p className="text-sm text-zinc-400 font-medium mb-2">
            {steps[currentStep].label}
          </p>
          <div className="flex items-baseline gap-3">
            <span
              className={cn(
                "text-4xl font-bold tabular-nums",
                steps[currentStep].color
              )}
            >
              {steps[currentStep].count.toLocaleString()}
            </span>
            <span className="text-sm text-zinc-500">
              {steps[currentStep].percentage}
            </span>
          </div>
          <p className="text-sm text-zinc-400 mt-3 leading-relaxed">
            {steps[currentStep].description}
          </p>
        </motion.div>
      </AnimatePresence>

      {/* Navigation */}
      <div className="flex items-center justify-between mt-4">
        <button
          onClick={() => canGoBack && setCurrentStep((s) => s - 1)}
          disabled={!canGoBack}
          className={cn(
            "px-4 py-2 rounded-lg text-sm font-medium transition-colors",
            canGoBack
              ? "bg-zinc-800 text-zinc-200 hover:bg-zinc-700"
              : "bg-zinc-900 text-zinc-700 cursor-not-allowed"
          )}
        >
          Back
        </button>
        <span className="text-xs text-zinc-600">
          {currentStep === steps.length - 1
            ? "End of funnel"
            : "Click Next to continue"}
        </span>
        <button
          onClick={() => canGoForward && setCurrentStep((s) => s + 1)}
          disabled={!canGoForward}
          className={cn(
            "px-4 py-2 rounded-lg text-sm font-medium transition-colors",
            canGoForward
              ? "bg-emerald-600 text-white hover:bg-emerald-500"
              : "bg-zinc-900 text-zinc-700 cursor-not-allowed"
          )}
        >
          Next
        </button>
      </div>
    </div>
  );
}
